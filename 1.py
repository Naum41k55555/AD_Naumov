# Открываем файл '1.txt' в режиме чтения ('r' по умолчанию)
f = open('1.txt')

# Читаем всё содержимое файла в строку s_in
s_in = f.read()

# Создаём пустую строку для результата сжатия
s_out = ""

# Счётчик повторяющихся символов. Начинаем с 1, так как любой символ уже есть
count = 1

# Проходим по всем символам строки, кроме последнего (до len(s_in)-1)
for i in range(0, len(s_in) - 1):
    # Сравниваем текущий символ со следующим
    if s_in[i] == s_in[i + 1]:
        # Если символы одинаковые, увеличиваем счётчик повторов
        count = count + 1
    else:
        # Если символы разные, значит серия одинаковых символов закончилась

        # Если счётчик больше 1, добавляем количество повторов в результат
        if (count != 1):
            s_out += str(count)

        # Добавляем сам символ в результат
        s_out += s_in[i]

        # Промежуточный вывод (для отладки)
        print(s_out)

        # Сбрасываем счётчик для новой серии символов
        count = 1

# Обработка последней серии символов после завершения цикла
if (count != 1):
    s_out += str(count)

# Добавляем последний символ строки (он не был обработан в цикле)
s_out += s_in[len(s_in) - 1]

# Выводим окончательный результат сжатия
print(s_out)

# Закрываем файл
f.close()