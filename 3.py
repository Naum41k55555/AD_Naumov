# Открываем файл '3.txt' для чтения
f = open("3.txt")

# Читаем первую строку, преобразуем в целое число - количество элементов
N = int(f.readline().strip())

# Читаем N следующих строк, преобразуем каждую в целое число
# и создаём список array из этих чисел
array = []
for i in range(N):
    num = int(f.readline().strip())
    array.append(num)

# Закрываем файл после чтения всех данных
f.close()

# Сортируем массив по возрастанию (для вычисления квартилей)
array.sort()

# Функция для вычисления медианы (серединного значения) массива
def median(arr):
    n = len(arr)
    if n % 2 == 0:
        # Если количество элементов чётное, медиана - среднее двух центральных
        return (arr[n // 2 - 1] + arr[n // 2]) / 2
    else:
        # Если нечётное, медиана - центральный элемент
        return arr[n // 2]

# Вычисляем второй квартиль Q2 (медиану всего набора данных)
if N % 2 == 0:
    # Для чётного N: среднее двух центральных элементов
    Q2 = (array[N // 2 - 1] + array[N // 2]) / 2
    # Нижняя половина: все элементы до середины
    lower_half = array[:N // 2]
    # Верхняя половина: все элементы от середины
    upper_half = array[N // 2:]
else:
    # Для нечётного N: центральный элемент
    Q2 = array[N // 2]
    # Нижняя половина: элементы до центрального (не включая его)
    lower_half = array[:N // 2]
    # Верхняя половина: элементы после центрального
    upper_half = array[N // 2 + 1:]

# Вычисляем первый квартиль Q1 (медиану нижней половины данных)
Q1 = median(lower_half)

# Вычисляем третий квартиль Q3 (медиану верхней половины данных)
Q3 = median(upper_half)

# Вычисляем межквартильный размах (InterQuartile Range)
IQR = Q3 - Q1

# Вычисляем границы для выявления выбросов по правилу 1.5*IQR
# Любые значения ниже этой границы считаются выбросами
lower_bound = Q1 - 1.5 * IQR

# Любые значения выше этой границы считаются выбросами
upper_bound = Q3 + 1.5 * IQR

# Подсчитываем количество выбросов
# Проходим по всем элементам массива и считаем те, что вне границ
count = 0
for x in array:
    if x < lower_bound or x > upper_bound:
        count += 1

# Выводим количество выбросов
print(count)